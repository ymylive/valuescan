@app.route('/api/valuescan/login/browser/import', methods=['POST'])
def valuescan_import_browser_login():
    """
    [DEPRECATED] Receive ValueScan localStorage tokens from user browser.
    
    This endpoint is deprecated. Please use /api/valuescan/credentials to save
    credentials and /api/valuescan/login/auto for automatic login instead.
    """
    return jsonify({
        "success": False,
        "error": "此登录方式已废弃。请使用账号密码登录功能。",
        "deprecated": True,
        "alternative": "/api/valuescan/login/auto"
    }), 410

    # Original implementation kept below for reference
    payload = request.json or {}
    if not isinstance(payload, dict):
        return jsonify({"success": False, "error": "Invalid payload"}), 400

    nonce = (payload.get("nonce") or "").strip()
    if not nonce:
        return jsonify({"success": False, "error": "Missing nonce"}), 400

    ip = _client_ip()
    ok, err = _verify_signed_nonce(nonce, ip)
    if not ok:
        return jsonify({"success": False, "error": err or "Invalid or expired nonce"}), 400

    def _as_storage(obj):
        if isinstance(obj, dict):
            return obj
        if isinstance(obj, str):
            try:
                parsed = json.loads(obj)
                return parsed if isinstance(parsed, dict) else {}
            except Exception:
                return {}
        return {}

    localstorage = _as_storage(payload.get("localstorage") or {})
    sessionstorage = _as_storage(payload.get("sessionstorage") or {})

    def _looks_like_jwt(val: str) -> bool:
        parts = (val or "").split(".")
        return len(parts) == 3 and all(parts)

    def _pick_token(store: dict, prefer_keys) -> str:
        for key in prefer_keys:
            v = (store.get(key) or "").strip()
            if v:
                return v
        for k, v in store.items():
            if not isinstance(k, str):
                continue
            if "token" not in k.lower():
                continue
            sv = (v or "").strip() if isinstance(v, str) else ""
            if sv and _looks_like_jwt(sv):
                return sv
        return ""

    account_token = _pick_token(localstorage, ["account_token", "accountToken", "token", "access_token", "accessToken"])
    refresh_token = _pick_token(localstorage, ["refresh_token", "refreshToken", "refresh"])
    if not account_token:
        account_token = _pick_token(sessionstorage, ["account_token", "accountToken", "token", "access_token", "accessToken"])
    if not refresh_token:
        refresh_token = _pick_token(sessionstorage, ["refresh_token", "refreshToken", "refresh"])

    payload_url = (payload.get("url") or "").strip() if isinstance(payload.get("url"), str) else ""
    available_keys = sorted({*(k for k in localstorage.keys() if isinstance(k, str)), *(k for k in sessionstorage.keys() if isinstance(k, str))})[:50]
    if not account_token and not refresh_token:
        return jsonify({
            "success": False,
            "error": "No token found. Please login on https://www.valuescan.io then click the bookmarklet on that page (not on the dashboard).",
            "seen_url": payload_url,
            "available_keys": available_keys,
        }), 400

    # Normalize tokens file format
    normalized = dict(localstorage)
    if account_token:
        normalized["account_token"] = account_token
    if refresh_token:
        normalized["refresh_token"] = refresh_token
    normalized.setdefault("language", "en-US")
    normalized["imported_at"] = time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime())

    try:
        _write_json_file(VALUESCAN_TOKEN_FILE, normalized)
    except Exception as exc:
        return jsonify({"success": False, "error": f"Failed to save token file: {exc}"}), 500

    cookie_header = payload.get("cookies")
    cookies_list = None
    if isinstance(cookie_header, str) and cookie_header.strip():
        try:
            c = SimpleCookie()
            c.load(cookie_header)
            cookies_list = [{"name": k, "value": v.value, "domain": ".valuescan.io", "path": "/"} for k, v in c.items()]
        except Exception:
            cookies_list = None

    if cookies_list is not None:
        try:
            _write_json_file(VALUESCAN_COOKIES_FILE, cookies_list)
        except Exception:
            pass

    # Best-effort restart: signal monitor + token refresher
    try:
        if shutil.which("systemctl"):
            subprocess.run(["systemctl", "restart", "valuescan-signal"], timeout=20, check=False)
            subprocess.run(["systemctl", "restart", "valuescan-monitor"], timeout=20, check=False)
            subprocess.run(["systemctl", "restart", "valuescan-token-refresher"], timeout=20, check=False)
    except Exception:
        pass

    status = valuescan_login_status().get_json()  # type: ignore[union-attr]
    return jsonify({"success": True, "status": status})

